void init_screen(Adafruit_SSD1306 &display)
{

    if (!already_run)
    {
        display.clearDisplay();
        display.drawBitmap(0, 0, logo_iniciomenu, 128, 64, SSD1306_WHITE);
        display.display();

        startTime = millis();
        already_run = true;
    }

    if (already_run && !finished)
    {
        if (millis() - startTime >= eventInterval)
        {
            finished = true;
        }
    }
}

bool end_init_screen()
{
    return finished;
}

